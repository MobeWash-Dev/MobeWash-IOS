import Foundation
import UIKit

extension UITextField {
    
    convenience init(frame: CGRect, borderStyle: UITextBorderStyle, placeholder: String){
        self.init(frame: frame)
        self.borderStyle = borderStyle
        self.placeholder = placeholder
        
    }
    
    
    
    
    
    
}
